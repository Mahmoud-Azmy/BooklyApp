import 'package:bookly_app/core/models/book_model/book_model.dart';
import 'package:bookly_app/features/search/data/repos/search_repo.dart';
import 'package:equatable/equatable.dart';
import 'package:flutter_bloc/flutter_bloc.dart';

part 'search_books_state.dart';

class SearchBooksCubit extends Cubit<SearchBooksState> {
  SearchBooksCubit(this.searchRepo) : super(SearchBooksInitial());

  static SearchBooksCubit get(context) => BlocProvider.of(context);
  final SearchRepo searchRepo;
  Future<void> fetchSearchBooksResult({required String bookName}) async {
    emit(SearchBooksLoading());
    var result = await searchRepo.fetchSearchBooksResult(bookName: bookName);
    result.fold((failure) {
      emit(SearchBooksFailure(failure.errMessage));
    }, (books) {
      emit(SearchBooksSuccess(books));
    });
  }
}
